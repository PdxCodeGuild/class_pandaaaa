# Lab 2: Blog
# Let's make the template of a blog with a title, 
# top-nav, side-nav, and multiple posts. Use flexbox 
# and semantic elements (header, nav, section, and footer). 
# You can use a custom font, fancy colors, and make generate 
# fake content using an Ipsum.

# Make sure you have a responsive design that offers different, 
# legible layouts for phone, tablet, and desktop screen sizes. 
# You can test this in the broswer devtools. Don't forget to use 
# cascading min-width media queries for a mobile first design, and 
# remember to include the responsive meta tag in your HTML.

# It should look professional, but this does not mean it needs 
# to be complicated. A clean, simple, minimalist layout with custom
#  color pallate, fonts, and subtle backgrounds or whitespace works
#   great. For examples of simple yet attractive design, check out:

# http://www.geert.io/
# https://www.blog.google/
# http://www.nextportland.com/
# https://daringfireball.net/

# Suggested Steps:
# Create the HTML structure for your blog, with all content included.
# Create your mobile version, using the styling of your choice.
# Use min-width media queries to add a tablet layout using flexbox.
#  Customize your styling as needed. You probably want your breakpoint 
#  between phone and tablet widths to be somewhere around 640-768px.
# Finally, add your desktop layout. Customize styling as needed.
#  A good breakpoint for desktop screen sizes is around 960-1100px.
